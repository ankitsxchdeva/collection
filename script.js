  function showKeycapInfo(cellId) {
    const data = keycapData[cellId];
    if (data) {
      document.getElementById("keycap-image").src = data.image;
      
      const linkElement = document.getElementById("keycap-link");
      if (data.hasArchivistLink) {
        linkElement.style.display = "inline";
        linkElement.href = data.archivistLink;
      } else {
        linkElement.style.display = "none";
      }
      
      document.getElementById("info-panel").style.display = "block";
      grid.style.display = "none";
    } else {
      console.warn("No data found for cell:", cellId);
    }
  }
  
  
  // Back button to return to grid
  function closePanel() {
    document.getElementById("info-panel").style.display = "none";
    grid.style.display = "grid";
  }
  